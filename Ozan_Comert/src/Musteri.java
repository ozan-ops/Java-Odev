public class Musteri
{
	public String TC;
	public String Ad;
	public String Soyad;
	public String Telefon;
	public int Cinsiyet;
	public int Engel;

	public Musteri(String tc, String ad, String soyad, String telefon, int cinsiyet, int engel)
	{
	    this.TC = tc;
	    this.Ad = ad;
	    this.Soyad = soyad;
	    this.Telefon = telefon;
	    this.Cinsiyet = cinsiyet;
	    this.Engel = engel;
	}
}